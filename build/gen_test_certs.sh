#!/bin/bash -e

NAME="appqos-silpixa00401062.appqos"
#NAME="10.237.223.201"

echo -e "WARNING: TEST KEYS AND CERTIFICATES GENERATED BY THIS SCRIPT ARE FOR \
EVALUATING THE SETUP ONLY - NOT INTENDED FOR FOR A PRODUCTION USE\n"

set -x

# using test with 'sudo' because of case, where crt files are not visible for non-root users
if ! sudo test -f certs/public/ca.crt; then
	# Generate root CA cert
	openssl req -nodes -x509 -newkey rsa:3072 -keyout certs/public/ca.key -out certs/public/ca.crt -subj "/O=AppQoS/OU=root/CN=${NAME}"

    # giving access rights for the ca cert key only to the root user
    sudo chown root certs/public/ca.key
fi

if ! sudo test -f certs/public/appqos.crt; then
    openssl req -nodes -newkey rsa:3072 -keyout certs/public/appqos.key -out certs/public/appqos.csr -subj "/O=AppQoS/OU=AppQoS Server/CN=${NAME}"
    sudo openssl x509 -req -in certs/public/appqos.csr -CA certs/public/ca.crt -CAkey certs/public/ca.key -CAcreateserial -out certs/public/appqos.crt

    # changing the ownership of the appqos cert and key - to match credentials of the user that will launch
    # the AppQoS server
    sudo chown root certs/public/appqos.crt certs/public/appqos.csr certs/public/ca.key
fi

if ! sudo test -f certs/private/client_appqos.crt; then
    # Generate client_appqos cert and sign it
    openssl req -nodes -newkey rsa:3072 -keyout certs/private/client_appqos.key -out certs/private/client_appqos.csr -subj "/O=AppQoS/OU=AppQoS Client/CN=client_host"
    sudo openssl x509 -req -in certs/private/client_appqos.csr -CA certs/public/ca.crt -CAkey certs/public/ca.key -out certs/private/client_appqos.crt
fi
